/*
이 문제는 N벌의 상의와 N벌의 하의가 있을 때,
상의와 하의가 서로 다른 색상인 조합의 수를 찾는 것입니다
각 i 번째 상의와 i번째 하의는 동일한 색상 i를 가집니다
*/


#include<stdio.h>

int main() {
	int n;		// 상의와 하의의 벌 수를 나타내는 변수

	// 사용자로부터 N을 입력받음
	scanf("%d", &n);

	// 상의와 하의가 서로 다른 색상인 조합의 수를 계산
	// 첫 번째 상의를 선택하는 경우의 수 : N
	// 선택된 상의와 다른 색상의 하의를 선택하는 경우의 수 : N-1
	// 따라서 총 조합의 수는 N * (N-1)
	long long result = (long long)n * (n - 1);		// N이 최대 2017이므로 결과가 int 범위를 넘을 수 있어 long long 사용 고려
													// 2017 * 2016 = 4066272 이므로 int로도 충분하나, 안전하게 long long 사용.
													// 문제의 N 제한(2017)으로는 int도 가능합니다. (2017 * 2016 = 4,006,272)

	// 결과출력
	printf("%lld\n", result);		// long long 형식 지정자 %lld 사용

	return 0;		// 프로그램 정상 종료
}